std::string getMatchingMessageNames(DWORD msgId)
{
	auto [itrBegin, itrEnd] = windowsMessagesData.equal_range(msgId);
	if (itrBegin == windowsMessagesData.end())
	{
		return "<UNKNOWN>";
	}
	std::string output;
	while (itrBegin != itrEnd)
	{
		output += itrBegin->second.name;
		output += ", ";
		itrBegin++;
	}
	output.erase(output.size() - 2, 2);
	return output;
}
